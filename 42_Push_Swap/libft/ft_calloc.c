#include <stdlib.h>
#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	void	*ret_val;

	ret_val = (void *) malloc(size * count);
	if (!ret_val)
		return (NULL);
	ft_bzero(ret_val, count * size);
	return (ret_val);
}
